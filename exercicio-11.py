# Faça um Programa que peça 2 números inteiros e um número real. Calcule e mostre:

# entrada de dados:
num_1 = int(input("Digite um numero inteiro: "))
num_2 = int(input("Digite um numero inteiro: "))
numero_real = float(input("Digite um numero real: "))

# o produto do dobro do primeiro com metade do segundo .
print ('Soma:', ((2*num_1) * (num_2/2)))
# a soma do triplo do primeiro com o terceiro.
print ('Produto:', (3 * num_1) + numero_real)
# o terceiro elevado ao cubo.
print ('Cubo:', numero_real**3)